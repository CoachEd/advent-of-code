def printBoard(arr):
  s = ''
  for row in arr:
    for c in row:
      s = s + c
    s = s + '\n' 
  print(s)

def extendBoard(arr):
  for y in range(0,len(arr)):
      r = arr[y]
      if arr[y][len(arr)-1] == '#':
        r.append('X')
      elif y > 0 and arr[y-1][len(arr)-1] == '#':
        r.append('X')
      else:
        r.append('.')
  arr.append(['.' for x in range(len(arr)+1) ])


